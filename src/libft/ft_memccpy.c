#include "libft.h"

void	*ft_memccpy(void *restrict dst, const void *restrict src, int c,
					size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	i = 0;
	a = (unsigned char *)dst;
	b = (unsigned char *)src;
	while (i < n)
	{
		if ((a[i] = b[i]) == (unsigned char)c)
			return (a + i + 1);
		i++;
	}
	return (NULL);
}
